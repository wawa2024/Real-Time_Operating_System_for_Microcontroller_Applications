/////////////////////////////// 3.Types ////////////////////////////////

extern uint8_t conv2d(const char* p);

typedef struct {
  int8_t hh=conv2d(__TIME__)
    , mm=conv2d(__TIME__+3)
    , ss=conv2d(__TIME__+6)
    ; // Get H, M, S from compile time
} Time ;

typedef enum {
  TIME_READ,
  TIME_STOP,
  TIME_START,
  TIME_UNKNOWN
} timeMethod_t;

typedef struct {
  timeMethod_t method = TIME_UNKNOWN;
} timeRequest_t ;

typedef struct {
  Time time;
} timeResponse_t ;

